export const getFeaturedPost = (): BlogPost => {
  return blogPosts[0];
};

export const getPostBySlug = (slug: string): BlogPost | undefined => {
  return blogPosts.find(post => post.slug === slug);
};

export const getRelatedPosts = (currentPostId: string, category?: string): BlogPost[] => {
  // If category is provided, filter by it
  let filteredPosts = category 
    ? blogPosts.filter(post => post.categories.includes(category))
    : [...blogPosts];
  
  // Remove the current post
  filteredPosts = filteredPosts.filter(post => post.id !== currentPostId);
  
  // Return up to 3 posts
  return filteredPosts.slice(0, 3);
}; 